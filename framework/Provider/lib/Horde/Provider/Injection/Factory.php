class Horde_Provider_Injection_Factory extends Horde_Provider_Injection_Base
{
    /**
     * Constructor.
     *
     * @param mixed $factory A callable factory function.
     */
    public function __construct($factory)
    {
        $this->factory = $factory;
    }

    /**
     * Create an instance of the proxied object via the factory method.
     *
     * @param Horde_Provider_Base $provider The class providing additional
     *                                      required dependencies.
     *
     * @return mixed The instance generated by the factory.
     */
    public function getInstance(Horde_Provider_Base $provider)
    {
        parent::getInstance($provider);

        $instance = call_user_func($this->factory, $provider);

        $this->loading = false;

        return $instance;
    }
}